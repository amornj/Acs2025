export function searchGuidelines(query: string): GuidelineRecommendation[] {
  if (!query.trim()) return [];

  const terms = query.toLowerCase().split(/\s+/).filter(Boolean);

  const scored = GUIDELINE_RECOMMENDATIONS.map((rec) => {
    const searchable = [
      rec.title,
      rec.text,
      rec.section,
      ...rec.keywords,
    ].join(' ').toLowerCase();

    let score = 0;
    for (const term of terms) {
      if (searchable.includes(term)) {
        score += 1;
        // Bonus for keyword match
        if (rec.keywords.some((k) => k.includes(term))) score += 1;
        // Bonus for title match
        if (rec.title.toLowerCase().includes(term)) score += 2;
      }
    }

    return { rec, score };
  });

  return scored
    .filter((s) => s.score > 0)
    .sort((a, b) => b.score - a.score)
    .map((s) => s.rec);
}
